package com.array;

public class Two_diamensionalvalue {
	
	public static void main(String[] args) {
		
		int[][] a=new int[2][2];
		
		a[0][0]=12;
		a[0][1]=13;
		a[1][0]=14;
		a[1][1]=15;
		
		System.out.println(a[1][1]);  // find value of array
		System.out.println(a.length); // Size of array
	}

}
